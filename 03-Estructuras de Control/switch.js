//Sentencias switch

let nota = 1;

switch(nota){
    case 5:
        console.log("Buen trabajo");
        break;
    case 4:
        console.log("Buen trabajo pero podrias haberlo hecho mejo");
        break;
    case 3:
        console.log("Has obtenido un suficiente");
        break;
    case 2:
        console.log("No has aprobado por poco");
        break;
    case 1:
        console.log("No has estudiado nada");
        break;
    default:
        console.log("Error")
        break;
}